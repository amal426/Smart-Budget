export const localizeValue = (value: string | undefined | null): string => {
  if (!value) return "";
  const trimmed = value.trim();
  
  // Direct match
  if (valueTranslations[trimmed]) {
    return valueTranslations[trimmed][currentLanguage];
  }
  
  // Case-insensitive / normalized match
  const lower = trimmed.toLowerCase();
  for (const [key, mapping] of Object.entries(valueTranslations)) {
    if (key.toLowerCase() === lower) {
      return mapping[currentLanguage];
    }
  }
  
  return trimmed;
};

export const t = (key: string, variables?: Record<string, string | number>): string => {
  const dict = translations[currentLanguage] || translations["ar"];
  let text = dict[key] || translations["ar"][key] || key;
  if (variables) {
    Object.entries(variables).forEach(([k, v]) => {
      text = text.replace(new RegExp(`{${k}}`, "g"), String(v));
    });
  }
  return text;
};
