export type Language = "ar" | "en";

// Initialize language from LocalStorage, default is "ar"
export const currentLanguage: Language = (localStorage.getItem("smart_budget_lang") as Language) || "ar";

export function getLanguage(): Language {
  return currentLanguage;
}

export function setLanguage(lang: Language) {
  localStorage.setItem("smart_budget_lang", lang);
  window.location.reload();
}

// Format date: Hijri for Arabic, Gregorian for English
export function getFormattedDate(): string {
  const date = new Date();
  if (currentLanguage === "ar") {
    return new Intl.DateTimeFormat("ar-SA-u-ca-islamic", {
      day: "numeric",
      month: "long",
      year: "numeric"
    }).format(date);
  } else {
    return new Intl.DateTimeFormat("en-US", {
      day: "numeric",
      month: "long",
      year: "numeric"
    }).format(date);
  }
}

// Format currency: "ر.س" for Arabic, "SAR" for English
export function formatCurrency(amount: number): string {
  const formatted = amount.toLocaleString("en-US");
  if (currentLanguage === "ar") {
    return `${formatted} ر.س`;
  } else {
    return `${formatted} SAR`;
  }
}

